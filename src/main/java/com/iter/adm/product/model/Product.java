package com.iter.adm.product.model;

public class Product {
	
	private String productName;
	private String productCategoryCode;
	private double productRating;
	private double productPrice;
	private double discountPrice;
    private String inventoryStatus;
    private String ecommerceName;
	private double maxRetailPrice;
    private String ecommerceLink;
    private String ecommerceImage;
    private String productDescription;
    private String productImage;
    
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductCategoryCode() {
		return productCategoryCode;
	}
	public void setProductCategoryCode(String productCategoryCode) {
		this.productCategoryCode = productCategoryCode;
	}
	public double getProductRating() {
		return productRating;
	}
	public void setProductRating(double productRating) {
		this.productRating = productRating;
	}
	
	public double getProductPrice() {
		return productPrice;
	}
	public void setProductPrice(double productPrice) {
		this.productPrice = productPrice;
	}
	public double getDiscountPrice() {
		return discountPrice;
	}
	public void setDiscountPrice(double discountPrice) {
		this.discountPrice = discountPrice;
	}
	public String getInventoryStatus() {
		return inventoryStatus;
	}
	public void setInventoryStatus(String inventoryStatus) {
		this.inventoryStatus = inventoryStatus;
	}
	public String getEcommerceName() {
		return ecommerceName;
	}
	public void setEcommerceName(String ecommerceName) {
		this.ecommerceName = ecommerceName;
	}
	public double getMaxRetailPrice() {
		return maxRetailPrice;
	}
	public void setMaxRetailPrice(double maxRetailPrice) {
		this.maxRetailPrice = maxRetailPrice;
	}
	public String getEcommerceLink() {
		return ecommerceLink;
	}
	public void setEcommerceLink(String ecommerceLink) {
		this.ecommerceLink = ecommerceLink;
	}
	public String getEcommerceImage() {
		return ecommerceImage;
	}
	public void setEcommerceImage(String ecommerceImage) {
		this.ecommerceImage = ecommerceImage;
	}
	public String getProductDescription() {
		return productDescription;
	}
	public void setProductDescription(String productDescription) {
		this.productDescription = productDescription;
	}
	public String getProductImage() {
		return productImage;
	}
	public void setProductImage(String productImage) {
		this.productImage = productImage;
	}
    
    

}
